function checkEvenOdd() {
  let a = Number(document.getElementById("n4").value);
  let b = document.getElementById("nn4");
  if (a == 0) {
    b.innerText = "sefr";
    b.classList.add("seffr");
    b.classList.remove("highlight","ff");
   

    b.classList.remove("highlight");
  }
  else if (a % 2 == 0) {
    b.innerText = "zoje";
    b.classList.add("highlight");
    b.classList.remove("seffr","ff");
   
  } else {
    b.innerText = "fard";

    b.classList.add("ff");
    b.classList.remove("highlight");
    b.classList.remove("seffr");
  }
}

